def _weakness_topics(conn, user_id: str, max_recs: int = 5) -> List[Dict[str, Any]]:
    user_topics = _fetch_user_topics(conn, user_id)
    weakest = sorted(user_topics, key=lambda x: x.get("mastery", 0.0))[:max_recs]
    out: List[Dict[str, Any]] = []
    for t in weakest:
        metrics = {
            "mastery": t.get("mastery", 0.0), 
            "confidence": t.get("confidence", 0.0), 
            "interest": t.get("interest", 0.5)
        }
        out.append({
            "topic_id": t.get("topic_id"),
            "name": t.get("name"),
            "score": round(1.0 - t.get("mastery", 0.0), 3),
            "metrics": metrics,
            "reasons": ["weakness: low mastery"],
        })
    return out


def _safe_extract_json(raw_content: str) -> str:
    """Safely extracts JSON payload from potential markdown code fences."""
    content = raw_content.strip()
    if "```json" in content:
        content = content.split("```json", 1)[1].split("```", 1)[0].strip()
    elif "```" in content:
        content = content.split("```", 1)[1].split("```", 1)[0].strip()
    return content.strip("`").strip()


def _resilient_json_parse(raw_content: str) -> List[Dict[str, Any]]:
    """Attempts regular JSON loading, falling back to regex object recovery if truncated."""
    cleaned = _safe_extract_json(raw_content)
    try:
        return json.loads(cleaned)
    except Exception:
        # Regex fallback: Find valid looking JSON objects {"name": ..., "reason": ...}
        extracted = []
        matches = re.findall(r'\{\s*"name"\s*:\s*".*?"\s*,\s*"reason"\s*:\s*".*?"\s*\}', cleaned, re.DOTALL)
        for m in matches:
            try:
                extracted.append(json.loads(m))
            except Exception:
                continue
        if extracted:
            return extracted
        raise ValueError("JSON payload could not be recovered structurally.")


@tool
def recommend_topics_for_user(user_id: str, max_recs: int = 5, goal_texts: Optional[List[str]] = None, enable_llm: bool = True) -> Dict[str, Any]:
    """Recommend topics for a user strictly generated by the LLM based on their goals and currently studied topics without pulling from database candidate pools."""
    conn = get_conn()
    recommendations: List[Dict[str, Any]] = []
    try:
        user_topics = _fetch_user_topics(conn, user_id)
        studied_names = ", ".join([str(t.get("name")) for t in user_topics if t.get("name")])

        goals_local: List[str] = goal_texts or []
        if not goals_local:
            try:
                goals_local = _fetch_user_goals(conn, user_id)
            except Exception:
                goals_local = []
        
        joined_goals = "; ".join([g for g in goals_local if g]) if goals_local else "General professional growth"

        if enable_llm:
            prompt = (
                f"You are an advanced curriculum personalization assistant. "
                f"The user has already studied these topics: [{studied_names}]. "
                f"The user's current goals are: '{joined_goals}'.\n\n"
                f"Based strictly on this context, decide and generate a list of up to {max_recs} NEW topic names "
                f"that logically extend their knowledge and align with their goals. "
                f"Do NOT recommend topics already in their studied list, and do NOT use standard internal database topics.\n"
                f"Return ONLY a valid JSON array of strings (e.g., [\"Topic A\", \"Topic B\"]). No explanations, no numbering, no markdown fences."
            )

            llm = ChatGroq(model="llama-3.3-70b-versatile", api_key=SecretStr(os.getenv("GROQ_API_KEY") or ""), temperature=0.2)
            resp = llm.invoke([HumanMessage(content=prompt)])
            cleaned_json = _safe_extract_json(str(resp.content))
            
            gen = json.loads(cleaned_json)
            if isinstance(gen, list):
                for name in gen[:max_recs]:
                    if not isinstance(name, str) or not name.strip():
                        continue
                    recommendations.append({
                        "topic_id": None,
                        "name": name.strip(),
                        "score": 1.0,
                        "metrics": {"mastery": 0.0, "confidence": 0.0, "interest": 0.5},
                        "reasons": ["LLM dynamically suggested to bridge studied topics and goals"]
                    })
    except Exception:
        pass  
    finally:
        try:
            conn.close()
        except Exception:
            pass

    return {"recommendations": recommendations}


